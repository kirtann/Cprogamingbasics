#include <stdio.h>

int main()
{
    int a = 7;
    int b = 2;
    int sum, subtraction, multiplication, mod;
    int division;
    printf("sum of a and b =%d\n", sum = a + b);
    printf("subtraction of a and b =%d\n", subtraction = a - b);
    printf("multiplication of a and b =%d\n", multiplication = a * b);
    printf("division of a and b =%d\n", division = a / b);
    printf("modulus of a and b =%d", mod = a % b);
    return 0;
}
